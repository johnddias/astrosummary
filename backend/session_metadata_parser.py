def parse_session_metadata_directory(directory: str) -> SessionMetadataResponse:
    """
    Parse all Session Metadata Plugin files in a directory.

    Looks for:
    - AcquisitionDetails.json or AcquisitionDetails.csv
    - ImageMetaData.json or ImageMetaData.csv
    - WeatherData.json or WeatherData.csv
    """
    dir_path = Path(directory)
    if not dir_path.exists():
        logger.error(f"Directory not found: {directory}")
        return SessionMetadataResponse()

    acquisition_details = None
    image_metadata: List[ImageMetaDataRecord] = []
    weather_data: List[WeatherDataRecord] = []
    file_count = 0

    # Look for AcquisitionDetails
    for ext in [".json", ".csv"]:
        acq_file = dir_path / f"AcquisitionDetails{ext}"
        if acq_file.exists():
            if ext == ".json":
                acquisition_details = parse_acquisition_details_json(str(acq_file))
            else:
                acquisition_details = parse_acquisition_details_csv(str(acq_file))
            if acquisition_details:
                file_count += 1
            break

    # Look for ImageMetaData
    for ext in [".json", ".csv"]:
        img_file = dir_path / f"ImageMetaData{ext}"
        if img_file.exists():
            if ext == ".json":
                image_metadata = parse_image_metadata_json(str(img_file))
            else:
                image_metadata = parse_image_metadata_csv(str(img_file))
            if image_metadata:
                file_count += 1
            break

    # Look for WeatherData
    for ext in [".json", ".csv"]:
        weather_file = dir_path / f"WeatherData{ext}"
        if weather_file.exists():
            if ext == ".json":
                weather_data = parse_weather_data_json(str(weather_file))
            else:
                weather_data = parse_weather_data_csv(str(weather_file))
            if weather_data:
                file_count += 1
            break

    return SessionMetadataResponse(
        acquisition_details=acquisition_details,
        image_metadata=image_metadata,
        weather_data=weather_data,
        file_count=file_count,
    )


