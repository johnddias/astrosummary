def _is_nan_value(value: Any) -> bool:
    """Check if a value represents NaN (string 'NaN' or actual NaN)."""
    if value is None:
        return True
    if isinstance(value, str):
        return value.lower() == "nan" or value.strip() == ""
    if isinstance(value, float):
        return math.isnan(value)
    return False


def _safe_float(value: Any) -> Optional[float]:
    """Convert value to float, returning None for NaN/invalid values."""
    if _is_nan_value(value):
        return None
    try:
        result = float(value)
        if math.isnan(result):
            return None
        return result
    except (ValueError, TypeError):
        return None


def _safe_int(value: Any) -> Optional[int]:
    """Convert value to int, returning None for invalid values."""
    if _is_nan_value(value):
        return None
    try:
        return int(float(value))
    except (ValueError, TypeError):
        return None


def _normalize_field_names(record: Dict[str, Any], field_map: Dict[str, str]) -> Dict[str, Any]:
    """Normalize field names from plugin format to model format."""
    normalized = {}
    for key, value in record.items():
        # Try exact match first
        if key in field_map:
            normalized[field_map[key]] = value
        # Try lowercase match
        elif key.lower() in field_map:
            normalized[field_map[key.lower()]] = value
        else:
            # Keep original field with snake_case conversion
            snake_key = "".join(
                ["_" + c.lower() if c.isupper() else c for c in key]
            ).lstrip("_")
            normalized[snake_key] = value
    return normalized


def parse_acquisition_details_json(file_path: str) -> Optional[AcquisitionDetails]:
    """Parse AcquisitionDetails JSON file."""
    try:
        with open(file_path, "r", encoding="utf-8") as f:
            data = json.load(f)

        normalized = _normalize_field_names(data, ACQUISITION_FIELD_MAP)

        return AcquisitionDetails(
            target_name=normalized.get("target_name", "Unknown"),
            ra_coordinates=normalized.get("ra_coordinates"),
            dec_coordinates=normalized.get("dec_coordinates"),
            telescope_name=normalized.get("telescope_name"),
            focal_length=_safe_float(normalized.get("focal_length")),
            focal_ratio=_safe_float(normalized.get("focal_ratio")),
            camera_name=normalized.get("camera_name"),
            pixel_size=_safe_float(normalized.get("pixel_size")),
            bit_depth=_safe_int(normalized.get("bit_depth")),
            observer_latitude=_safe_float(normalized.get("observer_latitude")),
            observer_longitude=_safe_float(normalized.get("observer_longitude")),
            observer_elevation=_safe_float(normalized.get("observer_elevation")),
        )
    except Exception as e:
        logger.error(f"Error parsing AcquisitionDetails JSON: {e}")
        return None


